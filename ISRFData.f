	subroutine ReadISRF(lam,flux,nlam)
	use ISRFdata
	IMPLICIT NONE
	integer nlam
	real*8 lam(nlam),flux(nlam),x0,y0,x1,y1
	integer i,i0,j,k

	i=1
	k=1
1	x0=lamISRF(k)
	y0=F_ISRF(k)*x0
103	if(x0.ge.lam(i)) then
		flux(i)=y0
		i=i+1
		goto 103
	endif
100	k=k+1
	if(k.gt.nlamISRF) goto 102
	x1=lamISRF(k)
	y1=F_ISRF(k)*x1
101	if(lam(i).le.x1.and.lam(i).gt.x0) then
		if(y1.gt.1d-60.and.y0.gt.1d-60) then
			flux(i)=10d0**(log10(y1)+(log10(lam(i))-log10(x1))*(log10(y0)-log10(y1))/(log10(x0)-log10(x1)))
		else
			flux(i)=y1+(lam(i)-x1)*(y0-y1)/(x0-x1)
		endif
		i=i+1
		if(i.gt.nlam) goto 102
		goto 101
	endif
	x0=x1
	y0=y1
	goto 100
102	continue
	do j=i,nlam
		flux(j)=y1*x1**2/lam(j)**2
	enddo
	
	return
	end
	


