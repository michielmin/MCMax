C     .  2) Grevesse, N., Lambert, D.L., Sauval et al. (1991), A&A, 242, 488
C     .  3) Helling, Ch., Winters, J., Sedlmayr, E. (2000), A&A, 358, 651
C     .  4) Henning, Th., Stognienko, R. (1996), A&A, 311, 291 
